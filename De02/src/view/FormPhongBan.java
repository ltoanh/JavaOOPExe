    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ten = "", moTa = "";
        int heSo = Integer.parseInt((String) cbHeSo.getSelectedItem());
        boolean isOk = true;
        
        //nhap ten
        try{
            ten = tfTen.getText().replaceAll("\\s+", " ").trim();
            if(ten.length() == 0){
                throw new IOException();
            }
            String rgTen = "^[a-zA-Z]+$";
            String checkTen[] = ten.split(" ");
            for(String check : checkTen){
                if(!check.matches(rgTen))
                    throw new InputMismatchException();
            }
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Ten khong duoc de trong");
            isOk = false;
        } catch(InputMismatchException e){
            JOptionPane.showMessageDialog(null, "Ten sai dinh dang");
            isOk = false;
        }
        
        //nhap mo ta
        try{
            moTa = tfMoTa.getText().replaceAll("\\s+", " ").trim();
            if(moTa.length() == 0){
                throw new IOException();
            }
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Mo ta khong duoc de trong");
            isOk = false;
        }
        
        if(isOk){
            PhongBan phong = new PhongBan(ten, moTa, heSo);
            addTo(phong);
            btnFalse();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


