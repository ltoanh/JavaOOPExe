    @Override
    public <T> void showData(List<T> list, DefaultTableModel model) {
        model.setRowCount(0);
        for(T nv: list){
            if(nv instanceof PhongBan){
                this.model.addRow(((PhongBan) nv).toObject());
            }
        }
    }

    @Override
    public <T> void addTo(T t) {
        listPB.add((PhongBan) t);
        showData(listPB, model);
        controller.writeToFile(fileName, listPB);
    }

    @Override
    public <T> void showFromFile(List<T> list, DefaultTableModel model, String fileName) {
        list = controller.readFromFile(fileName);
        if(list.size() > 0){
            listPB = (List<PhongBan>) list;
            int ma = listPB.get(list.size() - 1).getMaPB();
            PhongBan.setsMa(ma + 1);
        }
        showData(list, model);
    }
