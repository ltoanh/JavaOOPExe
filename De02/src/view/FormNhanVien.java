    @Override
    public <T> void showData(List<T> list, DefaultTableModel model) {
        model.setRowCount(0);
        for(T nv: list){
            if(nv instanceof NhanVien){
                this.model.addRow(((NhanVien) nv).toObject());
            }
        }
    }

    @Override
    public <T> void addTo(T t) {
        listNV.add((NhanVien) t);
        showData(listNV, model);
        controller.writeToFile(fileName, listNV);
    }

    @Override
    public <T> void showFromFile(List<T> list, DefaultTableModel model, String fileName) {
        list = controller.readFromFile(fileName);
        if(list.size() > 0){
            listNV = (List<NhanVien>) list;
            int ma = listNV.get(listNV.size() - 1).getMaNV();
            NhanVien.setsMa(ma + 1);
        }
        showData(list, model);
    }
