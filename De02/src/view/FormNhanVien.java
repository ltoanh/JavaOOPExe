    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ten = "", diaChi = "", soDT = "";
        int bac = Integer.parseInt((String) cbBac.getSelectedItem());
        boolean isOk = true;
        
        //nhap ten
        try{
            ten = tfTen.getText().replaceAll("\\s+", " ").trim();
            if(ten.length() == 0){
                throw new IOException();
            }
            String rgTen = "^[a-zA-Z]+$";
            String checkTen[] = ten.split(" ");
            for(String check : checkTen){
                if(!check.matches(rgTen))
                    throw new InputMismatchException();
            }
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Ten khong duoc de trong");
            isOk = false;
        } catch(InputMismatchException e){
            JOptionPane.showMessageDialog(null, "Ten sai dinh dang");
            isOk = false;
        }
        //nhap dia chi
        try{
            diaChi = tfDiaChi.getText().replaceAll("\\s+", " ").trim();
            if(diaChi.length() == 0){
                throw new IOException();
            }
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Dia chi khong duoc de trong");
            isOk = false;
        } 
        //nhap so dien thoai
        try{
            soDT = tfSDT.getText().trim();
            if(soDT.length() == 0){
                throw new IOException();
            }
            String rgSo = "^[0-9]+$";
            if(!soDT.matches(rgSo))
                throw new InputMismatchException();
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "SDT khong duoc de trong");
            isOk = false;
        } catch(InputMismatchException e){
            JOptionPane.showMessageDialog(null, "SDT sai dinh dang");
            isOk = false;
        }
        
        if(isOk){
            NhanVien nv = new NhanVien(ten, diaChi, soDT, bac);
            addTo(nv);
            btnFalse();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


