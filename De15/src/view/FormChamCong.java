    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        int soNgay = 0;
        int maNV = 0;
        int maPB = 0;
        boolean isOk = true;
        try {
            try {
                maNV = (int) cbNV.getSelectedItem();
            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(null, "Khong co du lieu Nhan Vien");
                isOk = false;
            }
            try {
                maPB = (int) cbPB.getSelectedItem();

            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(null, "Khong co du lieu Phong ban");
                isOk = false;

            }
            if (!canAddDataToList(maNV)) {
                throw new IOException();
            }

            try {
                String ngayString = formatInputString(tfNgay.getText());
                if (ngayString.length() == 0) {
                    throw new IOException();
                }
                soNgay = Integer.parseInt(ngayString);
                if (soNgay <= 0 || soNgay > 31) {
                    throw new InputMismatchException();
                }
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "So ngay khong de trong");
                isOk = false;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "So ngay chi nhap so");
                isOk = false;
            } catch (InputMismatchException e) {
                JOptionPane.showMessageDialog(null, "So ngay trong khoang tu 1 den 31");
                isOk = false;
            }

            if (isOk) {
                try{
                    NhanVien nhanVien = findByMa(maNV, listNV);
                    PhongBan phong = findByMa(maPB, listPB);
                    BangDanhSachChamCong chamCong = new BangDanhSachChamCong(nhanVien, phong, soNgay);
                    addToList(chamCong, listCC);
                    clearData();
                    btnFalse();
                    IOFile.writeFile(FILECC, listCC);
                } catch(NullPointerException e){
                    JOptionPane.showConfirmDialog(null, "Khong tim thay du lieu");
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Nhan vien da tham gia trong phong ban khac");

        }
    }//GEN-LAST:event_btnLuuActionPerformed

