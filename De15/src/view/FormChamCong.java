    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
        // TODO add your handling code here:
        try{
            listCC = IOFile.readFile(FILECC);
            if(listCC.isEmpty())
                throw new InputMismatchException();
            Collections.sort(listCC, (c1, c2) -> {
                return c1.getNhanVien().getHoTen().compareToIgnoreCase(c2.getNhanVien().getHoTen());
            });
            showData(listCC, model);
        } catch(InputMismatchException e){
            JOptionPane.showMessageDialog(null, "Khong co du lieu");
            buttonGroup1.clearSelection();
        }
    }//GEN-LAST:event_jRadioButton1ActionPerformed

