    private String formatInputString(String s) {
        return s.replaceAll("\\s+", " ").trim();
    }

    private boolean canAddDataToList(int maNV) {
        for (BangDanhSachChamCong cc : listCC) {
            if (cc.getNhanVien().getMaNhanVien() == maNV) {
                return false;
            }
        }
        return true;
    }

    private <T> T findByMa(int ma, List<T> list) {
        for (T t : list) {
            if (t instanceof NhanVien) {
                if(((NhanVien) t).getMaNhanVien() == ma)
                    return t;
            } else if (t instanceof PhongBan) {
                if(((PhongBan) t).getMaPhongBan() == ma)
                    return t;
            }
        }
        return null;
    }
