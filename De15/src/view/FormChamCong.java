    private void initCbData() {
        listPB = new ArrayList<>();
        listNV = new ArrayList<>();
        try {
            listPB = IOFile.readFile(FILEPB);
            listNV = IOFile.readFile(FILENV);
            if (listPB.isEmpty() || listNV.isEmpty()) {
                throw new InputMismatchException();
            }
            initItemData(cbPB, listPB);
            initItemData(cbNV, listNV);
            btnTrue();
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Khong co du lieu");
        }

    }

    private <T> void initItemData(JComboBox cb, List<T> list) {
        cb.removeAllItems();
        for (T t : list) {
            if (t instanceof NhanVien) {
                cb.addItem(((NhanVien) t).getMaNhanVien());
            } else if (t instanceof PhongBan) {
                cb.addItem(((PhongBan) t).getMaPhongBan());
            }
        }
    }

