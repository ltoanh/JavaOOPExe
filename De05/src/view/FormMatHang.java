    @Override
    public <T> void addTo(T t) {
        list.add((MatHang) t);
        showData(list);
    }

    @Override
    public <T> void showData(List<T> ls) {
        model.setRowCount(0);
        for(T t : ls){
            if(t instanceof MatHang){
                MatHang m = (MatHang) t;
                model.addRow(m.toObject());
            }
        }
    }
