    private boolean canAddTo(int maKH, int maMH) {
        for (HoaDon h : listHD) {
            if (h.getKhachHang().getMa() == maKH && h.getMatHang().getMa() == maMH) {
                return false;
            }
        }
        return true;
    }

    private KhachHang findKHByMa(int ma) {
        for (KhachHang k : listKH) {
            if (k.getMa() == ma) {
                return k;
            }
        }
        return null;
    }

    private MatHang findMHByMa(int ma) {
        for (MatHang k : listMH) {
            if (k.getMa() == ma) {
                return k;
            }
        }
        return null;
    }

