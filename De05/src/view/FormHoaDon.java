    private void initData() {
        cbKH.removeAllItems();
        cbMH.removeAllItems();

        for (KhachHang k : listKH) {
            cbKH.addItem(k.getMa() + "");
        }

        for (MatHang h : listMH) {
            cbMH.addItem(h.getMa() + "");
        }
    }

    @Override
    public <T> void addTo(T t) {
        listHD.add((HoaDon) t);
        showData(listHD);
    }

    @Override
    public <T> void showData(List<T> ls) {
        model.setRowCount(0);
        for(T t : ls){
            if(t instanceof HoaDon){
                HoaDon h = (HoaDon) t;
                model.addRow(h.toObject());
            }
        }
    }
