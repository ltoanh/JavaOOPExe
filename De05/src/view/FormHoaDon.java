    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        clearData();
        try {
            if (listKH.isEmpty() || listMH.isEmpty()) {
                throw new IOException();
            }
            initData();
            btnTrue();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Du lieu khong ton tai");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int maKH = Integer.parseInt((String) cbKH.getSelectedItem());
        int maMH = Integer.parseInt((String) cbMH.getSelectedItem());
        int so = 0;
        boolean isOK = true;
        try {
            if (!canAddTo(maKH, maMH)) {
                throw new IOException();
            }
            so = Integer.parseInt(tfSo.getText());
            if (so <= 0) {
                throw new NumberFormatException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "KH da mua MH nay");
            isOK = false;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "So luong sai dinh dang");
            isOK = false;
        }

        if (isOK) {
            KhachHang khachHang = findKHByMa(maKH);
            MatHang matHang = findMHByMa(maMH);

            HoaDon h = new HoaDon(khachHang, matHang, so);
            addTo(h);
            IOFile.writeFile(fileHD, listHD);
            btnFalse();
            clearData();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

