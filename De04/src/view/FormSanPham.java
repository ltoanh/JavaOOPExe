    @Override
    public <T> void addTo(T t) {
        list.add((SanPham) t);
        showData(list);
    }

    @Override
    public <T> void showData(List<T> ls) {
        model.setRowCount(0);
        for (T t : ls) {
            if (t instanceof SanPham) {
                model.addRow(((SanPham) t).toObject());
            }
        }
    }
