    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
        try{
            listTC = IOFile.readFile(FILETC);
            if(listTC.isEmpty())
                throw new IOException();
            Collections.sort(listTC, (t1, t2) -> {
                return t1.getPhongVien().getHoTen().compareToIgnoreCase(t2.getPhongVien().getHoTen());
            });
            showData(listTC, model);
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Khong co du lieu");
            buttonGroup1.clearSelection();
        }
    }//GEN-LAST:event_jRadioButton1ActionPerformed

    private void jRadioButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton2ActionPerformed
        try{
            listTC = IOFile.readFile(FILETC);
            if(listTC.isEmpty())
                throw new IOException();
            Collections.sort(listTC, (t1, t2) -> {
                int so1 = t1.getSoLuong();
                int so2 = t2.getSoLuong();
                return so2 > so1 ? 1 : so2 < so1 ? -1 : 0;
            });
            showData(listTC, model);
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Khong co du lieu");
            buttonGroup1.clearSelection();
        }
    }//GEN-LAST:event_jRadioButton2ActionPerformed


