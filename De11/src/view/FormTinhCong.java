    private String formatInputString(String s) {
        return s.replaceAll("\\s+", " ").trim();
    }

    private boolean isExistData(int maPV, int maKB) {
        for (BangTinhCong tc : listTC) {
            if (tc.getPhongVien().getMaPhongVien() == maPV && tc.getKieuBaiViet().getMaKieuBai() == maKB) {
                return true;
            }
        }
        return false;
    }

    private boolean isOverData(int maPV) {
        int so = 0;
        for (BangTinhCong tc : listTC) {
            if (tc.getPhongVien().getMaPhongVien() == maPV) {
                so++;
            }
        }
        return so >= COUNT;
    }

    private <T> T findByMa(int ma, List<T> list) {
        for (T t : list) {
            if (t instanceof PhongVien) {
                if (((PhongVien) t).getMaPhongVien() == ma) {
                    return t;
                }
            } else if (t instanceof KieuBaiViet) {
                if (((KieuBaiViet) t).getMaKieuBai() == ma) {
                    return t;
                }
            }
        }
        return null;
    }

