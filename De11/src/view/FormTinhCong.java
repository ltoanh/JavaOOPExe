    private void initCbData() {
        
        listPV = new ArrayList<>();
        listKB = new ArrayList<>();
        try {
            listPV = IOFile.readFile(FILEPV);
            listKB = IOFile.readFile(FILEKB);
            
            if (listPV.isEmpty() || listKB.isEmpty()) {
                throw new InputMismatchException();
            }
            initItemToCb(cbKB, listKB);
            initItemToCb(cbPV, listPV);
            btnTrue();
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Khong co du lieu");
        }
    }

    private <T> void initItemToCb(JComboBox cb, List<T> list) {
        cb.removeAllItems();
        for (T t : list) {
            if (t instanceof PhongVien) {
                cb.addItem(((PhongVien) t).formatMa());
            } else if (t instanceof KieuBaiViet) {
                cb.addItem(((KieuBaiViet) t).formatMa());
            }
        }
    }

