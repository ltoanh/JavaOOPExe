    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        int maPV = Integer.parseInt((String) cbPV.getSelectedItem());
        int maKB = Integer.parseInt((String) cbKB.getSelectedItem());

        int soLuong = 0;
        boolean isOk = true;
        try {
            if (isExistData(maPV, maKB)) {
                throw new IOException();
            }
            if (isOverData(maPV)) {
                throw new InputMismatchException();
            }
            PhongVien pv = findByMa(maPV, listPV);
            KieuBaiViet kieuBaiViet = findByMa(maKB, listKB);
            try {
                String so = formatInputString(tfSo.getText());
                if (so.length() == 0) {
                    throw new InputMismatchException();
                }
                soLuong = Integer.parseInt(so);
                if(soLuong <= 0){
                    throw new IOException();
                }
            } catch (InputMismatchException e) {
                JOptionPane.showMessageDialog(null, "so luong khong de trong");
                isOk = false;
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "so luong phai lon hon 0");
                isOk = false;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "so luong sai dinh dang");
                isOk = false;
            }
            if(isOk){
                BangTinhCong tc = new BangTinhCong(pv, kieuBaiViet, soLuong);
                addToList(listTC, tc);
                IOFile.writeFile(FILETC, listTC);
                btnFalse();
                clearData();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "du lieu da ton tai");
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "phong vien da viet qua " + COUNT + " kieu bai");
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "khong co du lieu");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

