    @Override
    public <T> void addTo(T t) {
        list.add((MonHoc) t);
        showData(list, model);
    }

    @Override
    public <T> void showData(List<T> ls, DefaultTableModel md) {
        md.setRowCount(0);
        for (T t : ls) {
            if (t instanceof MonHoc) {
                md.addRow(((MonHoc) t).toObjects());
            }
        }
    }
