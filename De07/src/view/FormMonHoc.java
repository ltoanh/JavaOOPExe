    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String ten = "", soTc = "";
        int so = 0;
        boolean isOk = true;
        try {
            ten = formatStringInput(tfTen.getText());
            if (ten.length() == 0) {
                throw new IOException();
            }
            String regexTen = "^[a-zA-Z0-9]+$";
            if (!checkValidString(ten, regexTen)) {
                throw new InputMismatchException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Ten khong de trong");
            isOk = false;
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Ten sai dinh dang");
            isOk = false;
        }
        try {
            soTc = formatStringInput(tfSo.getText());
            if (soTc.length() == 0) {
                throw new IOException();
            }
            so = Integer.parseInt(soTc);
            if(so <= 0)
                throw new NumberFormatException();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "So dvht khong de trong");
            isOk = false;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "So dvht sai dinh dang");
            isOk = false;
        }
        String loai = (String) cbLoai.getSelectedItem();
        if(isOk){
            MonHoc h = new MonHoc(ten, so, loai);
            addTo(h);
            IOFile.writeFile(file, list);
            clearData();
            btnFalse();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

