    private String formatStringInput(String s) {
        return s.replaceAll("\\s+", " ").trim();
    }

    private boolean checkValidString(String s, String regex) {
        String checkArr[] = s.split(" ");
        for (String check : checkArr) {
            if (!check.matches(regex)) {
                return false;
            }
        }
        return true;
    }

