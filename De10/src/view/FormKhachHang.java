    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String ten = "";
        String loai = "", so = "";
        float mucTien = 0;
        int soXe = 0;
        boolean isOk = true;
        try {
            ten = formatStringInput(tfTen.getText());
            if (ten.length() == 0) {
                throw new IOException();
            }
            String regexTen = "^[a-zA-Z]+$";
            if (!isFormattedString(ten, regexTen)) {
                throw new InputMismatchException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Ten khong de trong");
            isOk = false;
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Ten sai dinh dang");
            isOk = false;
        }

        try {
            loai = formatStringInput(tfLoai.getText());
            if (loai.length() == 0) {
                throw new IOException();
            }
            String regexLoai = "^[a-zA-Z0-9]+$";
            if (!isFormattedString(ten, regexLoai)) {
                throw new InputMismatchException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Loai xe khong de trong");
            isOk = false;
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Loai xe sai dinh dang");
            isOk = false;
        }

        try {
            so = formatStringInput(tfSoXe.getText());
            if (so.length() == 0) {
                throw new IOException();
            }
            soXe = Integer.parseInt(so);
            if (soXe <= 0) {
                throw new InputMismatchException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "so xe khong de trong");
            isOk = false;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "so xe sai dinh dang");
            isOk = false;
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "so xe phai lon hon 0");
            isOk = false;
        }

        if (isOk) {
            KhachHang k = new KhachHang(ten, loai, soXe);
            addToList(list, k);
            IOFile.writeFile(FILE, list);
            clearData();
            btnFalse();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

