    private void initCbData() {
        cbKH.removeAllItems();

        listKH = new ArrayList<>();
        listOTO = new ArrayList<>();
        try {
            listKH = IOFile.readFile(FILEKH);
            listOTO = IOFile.readFile(FILEOTO);
            if (listKH.isEmpty() || listOTO.isEmpty()) {
                throw new InputMismatchException();
            }
            for (KhachHang k : listKH) {
                cbKH.addItem(k.formatMa());
            }
            btnTrue();
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Khong co du lieu");
        }
    }

    private void btnTrue() {
        btnLuu.setEnabled(true);
        btnHuy.setEnabled(true);
    }

    private void btnFalse() {
        btnLuu.setEnabled(false);
        btnHuy.setEnabled(false);
    }

    private KhachHang findKHByMa(int ma) {
        for (KhachHang k : listKH) {
            if (k.getMa() == ma) {
                return k;
            }
        }
        return null;
    }

    private OTo findOToByRequire(String xeMuonThue) {
        for (OTo xe : listOTO) {
            if (xe.getKieuXe().equalsIgnoreCase(xeMuonThue)) {
                return xe;
            }
        }
        return null;
    }

    private boolean isEnoughCarToRent(OTo xe, String require, int so) {
        int soXe = 0;
        for (BangSapXep xep : listSX) {
            if (xep.getoTo().getKieuXe().equalsIgnoreCase(require)) {
                soXe += xep.getKhachHang().getSoXe();
            }
        }
        return soXe + so <= xe.getSoXe();
    }

    private String formatStringInput(String s) {
        return s.replaceAll("\\s+", " ").trim();
    }

