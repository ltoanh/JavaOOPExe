    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        int maKH = Integer.parseInt((String) cbKH.getSelectedItem());
        int soNgayThue = 0;
        boolean isOk = true;

        try {
            KhachHang khach = findKHByMa(maKH);
            OTo xe = findOToByRequire(khach.getLoaiXeMuonThue());
            if (!isEnoughCarToRent(xe, xe.getKieuXe(), khach.getSoXe())) {
                throw new IOException();
            }
            try {
                String soNgay = formatStringInput(tfNgay.getText());
                if (soNgay.length() == 0) {
                    throw new IOException();
                }
                soNgayThue = Integer.parseInt(soNgay);
                if (soNgayThue <= 0) {
                    throw new NumberFormatException();
                }
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "So ngay khong de trong");
                isOk = false;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "So ngay sai dinh dang");
                isOk = false;
            }
            if (isOk) {
                BangSapXep xep = new BangSapXep(khach, xe, soNgayThue);
                addToList(listSX, xep);
                IOFile.writeFile(FILESX, listSX);
                clearData();
                btnFalse();
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Khong co loai xe mong muon");

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Khong co du xe cho thue");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

