    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        int maHK = 0;
        int maVe = 0;
        try {
            maHK = (int) cbHK.getSelectedItem();
            maVe = (int) cbVe.getSelectedItem();
            if(!canBuyTicket(maHK, maVe))
                throw new IOException();
            if(isOverToBuy(maHK)){
                throw new InputMismatchException();
            }
            HanhKhach khach = findByMaInList(maHK, listHK);
            LoaiVe ve = findByMaInList(maVe, listVE);
            BangKeBanVe ban = new BangKeBanVe(khach, ve);
            addToList(ban, listBV);
            btnFalse();
            IOFile.writeFile(FILEBV, listBV);
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Hanh khach da mua loai ve nay");
        } catch(InputMismatchException e){
            JOptionPane.showMessageDialog(null, "Khach hang da mua qua " + COUNT + " loai ve");
        } catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Khong tim thay du lieu");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

