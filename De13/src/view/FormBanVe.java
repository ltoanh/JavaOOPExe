    private void initCbData() {
        listHK = new ArrayList<>();
        listVE = new ArrayList<>();

        try {
            listHK = IOFile.readFile(FILEHK);
            listVE = IOFile.readFile(FILEVE);
            if (listHK.isEmpty() || listVE.isEmpty()) {
                throw new IOException();
            }
            btnTrue();
            initItemToCb(listHK, cbHK);
            initItemToCb(listVE, cbVe);

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "khong co du lieu");
        }
    }

    private <T> void initItemToCb(List<T> list, JComboBox cb) {
        cb.removeAllItems();
        for (T t : list) {
            if (t instanceof HanhKhach) {
                cb.addItem(((HanhKhach) t).getMaHK());
            } else if (t instanceof LoaiVe) {
                cb.addItem(((LoaiVe) t).getMaLoai());
            }
        }
    }

