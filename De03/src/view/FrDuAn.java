    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ten = "", phi = "";
        float kinhPhi = (float) 0.0;
        boolean isOk = true;
        try{
            ten = tfTen.getText().replaceAll("\\s+", " ").trim();
            if(ten.length() == 0){
                throw new IOException();
            }
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Ten khong de trong");
            isOk = false;
        }
        String kieu = (String) jComboBox1.getSelectedItem();
        try{
            phi = tfKinhPhi.getText().replaceAll("\\s+", " ").trim();
            if(phi.length() == 0)
                throw new IOException();
            kinhPhi = Float.parseFloat(phi);
            if(kinhPhi < 0)
                throw new NumberFormatException();
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Kinh phi khong de trong");
            isOk = false;
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Kinh phi sai dinh dang");            
            isOk = false;
        }
        
        if(isOk){
            DuAn d = new DuAn(ten, kieu, kinhPhi);
            addTo(d);
            IOFile.writeToFile(list, fileName);
            btnFalse();
            clearData();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


