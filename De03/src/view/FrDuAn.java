    @Override
    public <T> void addTo(T t) {
        list.add((DuAn) t);
        showData(list);
    }

    @Override
    public <T> void showData(List<T> ls) {
        model.setRowCount(0);
        for(T t : ls){
            if(t instanceof DuAn){
                model.addRow(((DuAn) t).toObject());
            }
        }
    }
