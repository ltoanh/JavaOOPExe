    private String formatInputString(String s) {
        return s.replaceAll("\\s+", " ").trim();
    }

    private boolean isExistData(int maNM, int maVe) {
        for (HoaDon hd : listHD) {
            if (hd.getNguoiMuaVe().getMaNguoiMua() == maNM && hd.getVe().getMaVe() == maVe) {
                return true;
            }
        }
        return false;
    }

    private boolean isOverType(int maNM) {
        int dem = 0;
        for (HoaDon hd : listHD) {
            if (hd.getNguoiMuaVe().getMaNguoiMua() == maNM) {
                dem++;
            }
        }
        return dem >= SOLOAI;
    }

    private boolean isOverTiketsNumber(int so) {
        return so > SOVE;
    }

    private <T> T findByMa(List<T> list, int ma) {
        for (T t : list) {
            if (t instanceof NguoiMuaVe) {
                if (((NguoiMuaVe) t).getMaNguoiMua() == ma) {
                    return t;
                }
            } else if (t instanceof LoaiVe) {
                if (((LoaiVe) t).getMaVe() == ma) {
                    return t;
                }
            }
        }
        return null;
    }

