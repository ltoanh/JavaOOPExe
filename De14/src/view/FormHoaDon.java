    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        String soLuong = "";
        int maNM = 0, maVe = 0;
        int so = 0;
        boolean isOk = true;

        try {
            maNM = (int) cbNguoi.getSelectedItem();
            maVe = (int) cbVe.getSelectedItem();
            if (isExistData(maNM, maVe)) {
                throw new IOException();
            }
            if (isOverType(maNM)) {
                throw new InputMismatchException();
            }
            try {
                soLuong = formatInputString(tfSo.getText());
                if (soLuong.length() == 0) {
                    throw new IOException();
                }
                so = Integer.parseInt(soLuong);
                if (so <= 0) {
                    throw new InputMismatchException();
                }
                if (isOverTiketsNumber(so)) {
                    throw new IllegalAccessException();
                }
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "so luong khong de trong");
                isOk = false;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "so luong phai la so");
                isOk = false;

            } catch (InputMismatchException e) {
                JOptionPane.showMessageDialog(null, "so luong phai lon hon 0");
                isOk = false;

            } catch (IllegalAccessException e) {
                JOptionPane.showMessageDialog(null, "so luong da vuot qua " + SOVE + "/lan mua");
                isOk = false;

            }
            if (isOk) {
                NguoiMuaVe nguoi = findByMa(listNM, maNM);
                LoaiVe ve = findByMa(listVE, maVe);
                HoaDon h = new HoaDon(nguoi, ve, so);
                addToList(h, listHD);
                clearData();
                IOFile.writeFile(FILEHD, listHD);
                btnFalse();
            }  
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Khong co du lieu");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Nguoi nay da mua ve loai nay");

        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Nguoi nay da mua ve qua " + SOLOAI + " loai ve");

        }

    }//GEN-LAST:event_btnLuuActionPerformed

