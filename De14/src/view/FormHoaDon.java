    private void initCbData() {
        listNM = new ArrayList<>();
        listVE = new ArrayList<>();
        try {
            listNM = IOFile.readFile(FILENM);
            listVE = IOFile.readFile(FILEVE);
            if (listNM.isEmpty() || listVE.isEmpty()) {
                throw new NullPointerException();
            }
            initItemToCb(cbNguoi, listNM);
            initItemToCb(cbVe, listVE);
            btnTrue();
            clearData();
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Khong co du lieu");
        }
    }

    private <T> void initItemToCb(JComboBox cb, List<T> list) {
        cb.removeAllItems();
        for (T t : list) {
            if (t instanceof NguoiMuaVe) {
                cb.addItem(((NguoiMuaVe) t).getMaNguoiMua());
            } else if (t instanceof LoaiVe) {
                cb.addItem(((LoaiVe) t).getMaVe());
            }
        }
    }

