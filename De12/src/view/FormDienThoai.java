    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        String hang = "", modelString = "", donGia = "";
        float gia = 0;
        boolean isOk = true;
        try {
            hang = formatStringInput(tfHang.getText());
            if (hang.length() == 0) {
                throw new IOException();
            }
            String regexTen = "^[a-zA-Z]+$";
            if (!isFormattedString(hang, regexTen)) {
                throw new InputMismatchException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Hang san xuat khong de trong");
            isOk = false;
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Hang san xuat sai dinh dang");
            isOk = false;
        }
        
        try {
            modelString = formatStringInput(tfModel.getText());
            if (modelString.length() == 0) {
                throw new IOException();
            }
            String regexModel = "^[a-zA-Z0-9]+$";
            if (!isFormattedString(modelString, regexModel)) {
                throw new InputMismatchException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Model khong de trong");
            isOk = false;
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Model sai dinh dang");
            isOk = false;
        }
        
        try {
            donGia = formatStringInput(tfGia.getText());
            if (donGia.length() == 0) {
                throw new IOException();
            }
            gia = Float.parseFloat(donGia);
            if (gia <= 0) {
                throw new InputMismatchException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Don gia khong de trong");
            isOk = false;
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Don gia phai lon hon 0");
            isOk = false;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Don gia phai la so");
            isOk = false;
        }
        
        if (isOk) {
            DienThoai d = new DienThoai(hang, modelString, gia);
            addToList(d, list);
            clearData();
            btnFalse();
            IOFile.writeFile(FILE, list);
        }
    }//GEN-LAST:event_btnLuuActionPerformed

