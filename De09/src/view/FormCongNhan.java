    private String stringInputFormat(String s){
        return s.replaceAll("\\s+", " ").trim();
    }
    
    private boolean isFormatted(String s, String regex){
        String checkArr[] = s.split(" ");
        for(String check : checkArr){
            if(!check.matches(regex))
                return false;
        }
        return true;
    }
