    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String ten = "", diaChi = "", soDT = "";
        boolean isOk = true;
        try{
            ten = stringInputFormat(tfTen.getText());
            if(ten.length() == 0)
                throw new IOException();
            String regexTen = "^[a-zA-Z]+$";
            if(!isFormatted(ten, regexTen)){
                throw new InputMismatchException();
            }
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Ten khong de trong");
            isOk = false;
        } catch(InputMismatchException e){
            JOptionPane.showMessageDialog(null, "Ten sai dinh dang");
            isOk = false;
        }
        try{
            diaChi = stringInputFormat(tfDiaChi.getText());
            if(diaChi.length() == 0)
                throw new IOException();
            String regexDiaChi = "^[a-zA-Z0-9/-]+$";
            if(!isFormatted(diaChi, regexDiaChi)){
                throw new InputMismatchException();
            }
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Dia chi khong de trong");
            isOk = false;
        } catch(InputMismatchException e){
            JOptionPane.showMessageDialog(null, "Dia chi sai dinh dang");
            isOk = false;
        }
        try{
            soDT = stringInputFormat(tfSoDT.getText());
            if(soDT.length() == 0)
                throw new IOException();
            String regexSo = "^[0-9]+$";
            if(!isFormatted(soDT, regexSo)){
                throw new InputMismatchException();
            }
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "SDT khong de trong");
            isOk = false;
        } catch(InputMismatchException e){
            JOptionPane.showMessageDialog(null, "SDT sai dinh dang");
            isOk = false;
        }
        int bac = Integer.parseInt((String) cbBac.getSelectedItem());
        if(isOk){
            CongNhan c = new CongNhan(ten, diaChi, soDT, bac);
            addToList(list, c);
            IOFile.writeFile(FILE, list);
            clearData();
            btnFalse();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

