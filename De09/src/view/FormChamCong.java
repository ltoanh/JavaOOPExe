    private String stringInputFormat(String s) {
        return s.replaceAll("\\s+", " ").trim();
    }

    private boolean isOver(int ma, int curNgay) {
        int ngay = 0;
        for (BangDanhSachChamCong cham : listCC) {
            if (cham.getCongNhan().getMa() == ma) {
                ngay += cham.getNgay();
            }
        }
        return ngay + curNgay > TIME;
    }

    private <T> T findByMa(int ma, List<T> list) {
        for (T t : list) {
            if (t instanceof CongNhan) {
                if (((CongNhan) t).getMa() == ma) {
                    return t;
                }
            } else if (t instanceof XuongSanXuat) {
                if (((XuongSanXuat) t).getMa() == ma) {
                    return t;
                }
            }
        }
        return null;
    }

