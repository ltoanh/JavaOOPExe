    private void initCbData() {
        listCN = new ArrayList<>();
        listCN = IOFile.readFile(FILECN);

        listXSX = new ArrayList<>();
        listXSX = IOFile.readFile(FILEXSX);

        try {
            if (listCN.isEmpty() || listXSX.isEmpty()) {
                throw new InputMismatchException();
            }
            initItemToCb(listCN, cbCN);
            initItemToCb(listXSX, cbXuong);
            btnTrue();
            clearData();
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Du lieu khong ton tai");
        }
    }

    private <T> void initItemToCb(List<T> list, JComboBox cb) {
        cb.removeAllItems();
        for (T t : list) {
            if (t instanceof CongNhan) {
                cb.addItem(((CongNhan) t).formatMa());
            } else if (t instanceof XuongSanXuat) {
                cb.addItem(((XuongSanXuat) t).formatMa());
            }
        }
    }

