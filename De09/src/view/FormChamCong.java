    private void rCNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rCNActionPerformed
        try {
            listCC = IOFile.readFile(FILECC);
            if (listCC.isEmpty()) {
                throw new InputMismatchException();
            }
            Collections.sort(listCC, (c1, c2) -> {
                return c1.getCongNhan().getTen().compareToIgnoreCase(c2.getCongNhan().getTen());
            });
            showData(listCC, model);
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "khong co du lieu");
            buttonGroup1.clearSelection();
        }
    }//GEN-LAST:event_rCNActionPerformed

