    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        int maCN = Integer.parseInt((String) cbCN.getSelectedItem());
        int maXuong = Integer.parseInt((String) cbXuong.getSelectedItem());
        int ngay = 0;
        boolean isOk = true;
        try {
            String ngayLam = stringInputFormat(tfSo.getText());
            if (ngayLam.length() == 0) {
                throw new IOException();
            }
            ngay = Integer.parseInt(ngayLam);
            if (ngay <= 0 || ngay > 30) {
                throw new NumberFormatException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "so ngay khong de trong");
            isOk = false;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "so ngay sai dinh dang");
            isOk = false;
        }
        if (isOk) {
            try {
                if (isOver(maCN, ngay)) {
                    throw new InputMismatchException();
                }
                XuongSanXuat xuong = findByMa(maXuong, listXSX);
                CongNhan congNhan = findByMa(maCN, listCN);
                BangDanhSachChamCong cc = new BangDanhSachChamCong(congNhan, xuong, ngay);
                addToList(listCC, cc);
                IOFile.writeFile(FILECC, listCC);
                clearData();
                btnFalse();
            } catch (InputMismatchException e) {
                JOptionPane.showMessageDialog(null, "So ngay da vuot qua cho phep");
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

