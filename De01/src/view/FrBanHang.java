    private void initDataCbMH() {
        listMH = new ArrayList<>();
        cbMH.removeAllItems();
        listMH = IOFile.readFromFile(fileMH);
        if (listMH.size() > 0) {
            for (MatHang h : listMH) {
                cbMH.addItem(h.formatMa());
            }
        }
    }

    private void initDataCbKH() {
        listKH = new ArrayList<>();
        cbKH.removeAllItems();
        listKH = IOFile.readFromFile(fileKH);
        if (listMH.size() > 0) {
            for (KhachHang h : listKH) {
                cbKH.addItem(h.formatMa());
            }
        }
    }

