    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int maKH = Integer.parseInt((String) cbKH.getSelectedItem());
        int maMH = Integer.parseInt((String) cbMH.getSelectedItem());
        int so = 0;
        boolean isOk = true;
        try {
            so = Integer.parseInt(jTextField1.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "So luong sai dinh dang");
            isOk = false;
        }
        if (isOk) {
            KhachHang k = findKHByMa(maKH);
            MatHang h = findMHByMa(maMH);
            try {
                if (isExistData(maKH, maMH)) {
                    throw new IOException();
                }
                if (isOverPerTime(maKH)) {
                    throw new InputMismatchException();
                }
                BanHang ban = new BanHang(k, h, so);
                addTo(ban);
                clearData();
                btnFalse();
                IOFile.writeToFile(listBH, fileBH);
            } catch (InputMismatchException e) {
                JOptionPane.showMessageDialog(null, "Moi KH chi mua 10 loai MH");
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "KH da mua loai MH nay");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

