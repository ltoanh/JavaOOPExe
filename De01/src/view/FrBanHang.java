    @Override
    public <T> void addTo(T t) {
        BanHang bh = (BanHang) t;
        listBH.add(bh);
        dsMuaHang.put(bh.getHang(), bh.getSo());
        showData(listBH, model);
    }

    @Override
    public <T> void showData(List<T> ls, DefaultTableModel model) {
        model.setRowCount(0);
        for (T t : ls) {
            this.model.addRow(((BanHang) t).toObject());
        }
    }
