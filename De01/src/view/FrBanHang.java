    private KhachHang findKHByMa(int ma) {
        for (KhachHang k : listKH) {
            if (k.getMa() == ma) {
                return k;
            }
        }
        return null;
    }

    private MatHang findMHByMa(int ma) {
        for (MatHang h : listMH) {
            if (h.getMa() == ma) {
                return h;
            }
        }
        return null;
    }

    private boolean isOverPerTime(int maKH) {
        int dem = 0;
        for (BanHang bh : listBH) {
            int ma = bh.getKhachHang().getMa();
            if (ma == maKH) {
                dem++;
            }
        }
        return dem == MAXNUMBERPERTIME;
    }

    private boolean isExistData(int maKH, int maMH) {
        for (BanHang bh : listBH) {
            if (bh.getHang().getMa() == maMH && bh.getKhachHang().getMa() == maKH) {
                return true;
            }
        }
        return false;
    }

    private void sortByKH() {
        Collections.sort(listBH, (bh1, bh2) -> {
            return bh1.getKhachHang().getHoTen().compareToIgnoreCase(bh2.getKhachHang().getHoTen());
        });
        showData(listBH, model);
    }

    private void sortByMH() {
        Collections.sort(listBH, (bh1, bh2) -> {
            return bh1.getHang().getTen().compareToIgnoreCase(bh2.getHang().getTen());
        });
        showData(listBH, model);
    }

