    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ten = "", diaChi = "", soDT = "";
        boolean isOk = true;
        
        try {
            ten = tfTen.getText().replaceAll("\\s+", " ").trim();
            if (ten.length() == 0) {
                throw new IOException();
            }
            String checkTen[] = ten.split(" ");
            String regexTen = "^[A-Za-z]+$";
            for (String check : checkTen) {
                if (!check.matches(regexTen)) {
                    throw new InputMismatchException();
                }
            }
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "Ten sai dinh dang");
            isOk = false;
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Ten khong de trong");
            isOk = false;
        }

        try {
            diaChi = tfDiaChi.getText().replaceAll("\\s+", " ");
            if (diaChi.length() == 0) {
                throw new IOException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Dia chi khong de trong");
            isOk = false;
        }

        try {
            soDT = tfSo.getText().replaceAll("\\s+", " ");
            if (soDT.length() == 0) {
                throw new IOException();
            }
            String checkSo[] = soDT.split(" ");
            if (checkSo.length != 1) {
                throw new InputMismatchException();
            }
            String regexSo = "^[0-9]+$";
            if (!soDT.matches(regexSo)) {
                throw new InputMismatchException();
            }

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "So dien thoai khong de trong");
            isOk = false;
        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "So dien thoai sai dinh dang");
            isOk = false;
        }
        
        if(isOk){
            KhachHang khach = new KhachHang(ten, diaChi, soDT);
            addTo(khach);
            IOFile.writeToFile(list, fileName);
            clearData();
            btnFalse();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


