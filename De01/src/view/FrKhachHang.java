    @Override
    public <T> void addTo(T t) {
        if (t instanceof KhachHang) {
            this.list.add((KhachHang) t);
        }
        showData(list, model);
    }

    @Override
    public <T> void showData(List<T> ls, DefaultTableModel model) {
        model.setRowCount(0);
        for (T t : ls) {
            this.model.addRow(((KhachHang) t).tObject());
        }
    }
