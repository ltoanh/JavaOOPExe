    private void initDataCb() {
        jComboBox1.removeAllItems();
        for (KhachHang k : listKH) {
            jComboBox1.addItem(k.getMa() + "");
        }

        jComboBox2.removeAllItems();
        for (DichVu k : listDV) {
            jComboBox2.addItem(k.getMa() + "");
        }
    }

    private KhachHang findKhachByMa(int maKH) {
        for (KhachHang k : listKH) {
            if (k.getMa() == maKH) {
                return k;
            }
        }
        return null;
    }

    private DichVu findDVByMa(int maDV) {
        for (DichVu k : listDV) {
            if (k.getMa() == maDV) {
                return k;
            }
        }
        return null;
    }

    @Override
    public <T> void addTo(T t) {
        list.add((HoaDon) t);
        showData(list, model);
    }

    @Override
    public <T> void showData(List<T> ls, DefaultTableModel md) {
        md.setRowCount(0);
        for (T t : ls) {
            if (t instanceof HoaDon) {
                md.addRow(((HoaDon) t).toObject());
            }
        }
    }
