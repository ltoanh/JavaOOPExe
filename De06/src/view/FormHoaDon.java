    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int maKH = Integer.parseInt((String) jComboBox1.getSelectedItem());
        int maDV = Integer.parseInt((String) jComboBox2.getSelectedItem());
        int so = 0;
        String soLuong = "";
        boolean isOk = true;

        try {
            if (!canAdd(maKH, maDV)) {
                throw new IOException();
            }
            if (isOverPerTime(maKH)) {
                throw new InputMismatchException();
            }
            try {

                soLuong = tfSo.getText().replaceAll("\\s+", " ").trim();
                if (soLuong.length() == 0) {
                    throw new IOException();
                }
                so = Integer.parseInt(soLuong);
                if (so <= 0) {
                    throw new NumberFormatException();
                }
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "So luong khong de trong");
                isOk = false;

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "So luong sai dinh dang");
                isOk = false;

            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "KH da mua DV nay");
            isOk = false;

        } catch (InputMismatchException e) {
            JOptionPane.showMessageDialog(null, "KH da mua qua so luong cho phep");
            isOk = false;

        }

        if (isOk) {
            KhachHang k = findKhachByMa(maKH);
            DichVu d = findDVByMa(maDV);
            HoaDon h = new HoaDon(k, d, so);
            addTo(h);
            IOFile.writeFile(fileHD, list);
            clearData();
            btnFalse();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

