    private boolean canAdd(int maKH, int maDV) {
        for (HoaDon d : list) {
            if (d.getKhach().getMa() == maKH && d.getDichVu().getMa() == maDV) {
                return false;
            }
        }
        return true;
    }

    private boolean isOverPerTime(int maKH) {
        int count = 0;
        for (HoaDon d : list) {
            if (d.getKhach().getMa() == maKH) {
                count++;
            }
        }
        return count >= OVER;
    }

