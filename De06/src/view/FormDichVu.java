    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ten = "", donGia = "", donVi = "";
        float gia = 0;
        int tinh = 0;
        boolean isOk = true;
        try {
            ten = tfTen.getText().replaceAll("\\s+", " ").trim();
            if (ten.length() == 0) {
                throw new IOException();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Ten khong de trong");
            isOk = false;
        }

        try {
            donGia = tfGia.getText().replaceAll("\\s+", " ").trim();
            if (donGia.length() == 0) {
                throw new IOException();
            }
            gia = Float.parseFloat(donGia);
            if(gia <= 0)
                throw new NumberFormatException();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Don gia khong de trong");
            isOk = false;

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Don gia sai dinh dang");
            isOk = false;

        }

        try {
            donVi = tfDonVi.getText().replaceAll("\\s+", " ").trim();
            if (donVi.length() == 0) {
                throw new IOException();
            }
            tinh = Integer.parseInt(donVi);
            if(tinh <= 0)
                throw new NumberFormatException();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Don vi khong de trong");
            isOk = false;

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Don vi sai dinh dang");
            isOk = false;

        }
        
        if(isOk){
            DichVu k = new DichVu(ten, gia, tinh);
            addTo(k);
            IOFile.writeFile(file, list);
            clearData();
            btnFalse();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

