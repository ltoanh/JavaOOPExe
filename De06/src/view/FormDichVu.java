    @Override
    public <T> void addTo(T t) {
        list.add((DichVu) t);
        showData(list, model);
    }

    @Override
    public <T> void showData(List<T> ls, DefaultTableModel md) {
        md.setRowCount(0);
        for (T t : ls) {
            if (t instanceof DichVu) {
                md.addRow(((DichVu) t).toObject());
            }
        }
    }
